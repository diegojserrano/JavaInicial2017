public class DosVariables {

    public static void main(String[] args) {

        int a, b;
        
        a = 28;
        b = 42;
        
        int suma = a + b;
        int diferencia = a - b;
        int producto = a * b;
        
        System.out.println("La suma es: " + suma);
        System.out.println("La diferencia es: " + diferencia);
        System.out.println("El producto es: " + producto);
    }
    
}
